package BOJ.Mathematics;

import java.util.Scanner;
public class No_10869 {
    static Scanner sc=new Scanner(System.in);
    public static void main(String[] args) {
        int A = sc.nextInt();
        int B = sc.nextInt();

        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A/B);
        System.out.println(A%B);
    }
}